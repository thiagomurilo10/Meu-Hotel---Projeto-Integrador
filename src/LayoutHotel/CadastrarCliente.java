    private void botaoCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoCadastrarActionPerformed

        String nome = txtNome.getText();
        String telefone = txtTelefone.getText();
        String cpf = txtCPF.getText();
        String data = txtDataConsulta.getText();
        String jaEraPaciente = "";
        if (checkBoxPaciente.isSelected()) {
            jaEraPaciente = "Sim";
        } else {
            jaEraPaciente = "Não";
        }

        DadosHotel hotel = new DadosHotel();

        if (!emptyFields()) {
            if (!strTestes()) {
                hotel.setNome(nome);
                hotel.setTelefone(telefone);
                hotel.setCpf(cpf);
                hotel.setDataDaConsulta(data);
                hotel.setJaEraPaciente(jaEraPaciente);
            }
        }

        HotelDao hotelDAO = new HotelDao();
        hotelDAO.salvar(hotel);

        //limpar os campos
        txtNome.setText("");
        txtTelefone.setText("");
        txtCPF.setText("");
        txtDataConsulta.setText("");
        checkBoxPaciente.setSelected(false);
        //posicionar o cursor para um próximo
        txtNome.requestFocus();
    }//GEN-LAST:event_botaoCadastrarActionPerformed

