    private boolean strTestes() {
        boolean empty = true;

        if (txtTelefone.getText().matches("[(][0-9]{2}[)][0-9]{5}[-][0-9]{4}") == false) {
            JOptionPane.showMessageDialog(rootPane, "ATENÇÃO! Telefone tem que ser no formato (XX)XXXXX-XXXX");
        } else if (txtCPF.getText().matches("[0-9]{3}[.][0-9]{3}[.][0-9]{3}[-][0-9]{2}") == false) {
            JOptionPane.showMessageDialog(rootPane, "ATENÇÃO! CPF tem que ser no formato XXX.XXX.XXX-XX");
        } else if (txtDataConsulta.getText().matches("[0-3]{1}[0-9]{1}[/][0-1]{1}[0-9]{1}[/][0-2]{1}[0-9]{3}") == false) {
            JOptionPane.showMessageDialog(rootPane, "ATENÇÃO! Data agendada precisa ser válida e no formato XX/XX/XXXX");
        } else {
            empty = false;
        }

        return empty;
    }

